# 복합연산자는 어떤 값을 축적할때 주로 사용된다.
# 프로그래밍에서 자주볼 수 있는 표현이므로 익숙해질 필요가 있다!!!
a = 10
b = 'a'

a += 3   # a = a + 3
a -= 4   # a = a - 4
a *= 2   # a = a * 2
a /= 5   # a = a / 5
a //= 3  # a = a // 3
a **= 4  # a = a ** 4
b += 'c' # b = b + 'c'
b *= 4   # b = b * 4

# 카톡 방탈출에서 A = A+100 다 기억나시죠!!