# N을 입력받고, 2에서 N까지 수의 약수를 출력해라
N = int(input("수 입력 : "))

for i in range(2,N+1):
    print(i,'의 약수는',end='')
    for j in range(1,i+1):
        if i % j == 0:
            print(j, end=' ')
    print()




