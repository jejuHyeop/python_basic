print(type(1))        # 정수형 : 소수점 없는 수  >  int 
print(type(1.3))      # 실수형 : 소수점 있는 수  >  float
print(type(1-2j))     # 복소수형 complex
print(type('hello'))  # 문자열형 : 작은 따옴표나 큰따옴표 사이의 값이 들어간 형태  > str
print(type("World"))  # 문자열형 : 작은 따옴표나 큰따옴표 사이의 값이 들어간 형태  > str
