# 조건문과 반복문을 배우기 전에 파이썬 자료형 Bool 을 배워봅시다.
# 간단히 bool 형은 True(참), False(거짓) 를 나타내는 자료형입니다.
# 언어에 따라 true, True 와 같이 대소문자 구분을 엄격하게 합니다.
# python 에서는 True 꼭 이모양을 지켜주어야 Bool 값으로 인식합니다.

bool1 = True  # bool1 이라는 변수에 True (Bool자료형) 값을 대입합니다.
bool2 = False # bool2 이라는 변수에 False (Bool자료형) 값을 대입합니다.

print(type(bool1)) # class 'bool' 을 확인해보세요
print(type(bool2)) # 자료형을 확인하는것은 type 함수!! 

# bool 값은 참과 거짓 두 가지 값만을 가집니다!!
 

