# 주석이란 코드를 설명해놓은 것!!
# 사용 목적 : 내가 짠 코드를 나중에도 알아볼 수 있도록 
# 실무에서 인수인계(일을 물려주다)를 할 때, 주석을 해놓으면 후임자가 이해하기 쉬움
# 한줄 주석 : # 기호 뒤에 있는 모든 자료들은 주석이 된다.
'''
 여러줄 주석 : 작은따옴표 3개 혹은 큰따옴표 3개로 둘러싸인 자료들은 주석이 된다.

 여기도 주석
'''
"""
큰 따옴표도
마찬가지로
주석처리 됩니다.
"""
print(10) # 정수 자료형을 출력합니다.
print(1.333) # 실수 자료형을 출력합니다.  이런식으로 활용합니다.

# 하지만, 여러줄 주석의 경우에는 문자열로 활용될 수 있습니다.
a = """
동해물과
백두산이
마르고
닳도록
"""
print(a)
# 실행해보면 출력되는 것을 볼 수 있죠?
# 주석보다는 여러줄 문자열로 기억합시다!





